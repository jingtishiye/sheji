
function usertype(a){
	var a=1;
	if(a==1){
		document.getElementById("pic").style.display='block';
		document.getElementById("picc").style.display='none';
		document.getElementById("piccc").style.display='none';
		document.getElementById("picccc").style.display='none';
	}else{
		document.getElementById("pic").style.display='block';
		document.getElementById("picc").style.display='block';
		document.getElementById("piccc").style.display='block';
		document.getElementById("picccc").style.display='block';
	}
}


